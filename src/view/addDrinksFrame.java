    public addDrinksFrame() {
        initComponents();
        this.setLocationRelativeTo(null);
        drinksService = new DrinksService();
        List<Drinks> listD = drinksService.getAllDrinks();
        int ID = 1;
        boolean checkID;
        while (true) {
            checkID = false;
            for (Drinks d : listD) {
                if (ID == d.getIDdrinks()) {
                    checkID = true;
                    break;
                }
            }
            if (!checkID) {
                break;
            }
            ID++;
        }

        IDDrinksTextField.setText("" + ID);
    
    }

