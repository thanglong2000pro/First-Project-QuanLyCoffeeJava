    public addUserFrame(int IDUser) {
        initComponents();
        this.setLocationRelativeTo(null);
        user = new User();
        userService = new UserService();
        u = userService.getUserByID(IDUser);
        List<User> listUser = userService.getAllUser();
        int IDuser = 1;
        boolean checkID;
        while (true) {
            checkID = false;
            for (User u : listUser) {
                if (IDuser == u.getIDUser()) {
                    checkID = true;
                    break;
                }
            }
            if (!checkID) {
                break;
            }
            IDuser++;
        }

        IDEtextField.setText("" + IDuser);

    }

