    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        if (nameTextField.getText().equals("")
                || ImportDateTextField.getText() == null || priceTextField.getText() == null
                || amoutTextField.getText() == null) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đầy đủ thông tin!");
        } else {

            String pattern = "\\d$";

            Pattern r = Pattern.compile(pattern);
            Matcher m = r.matcher(priceTextField.getText());
            Matcher m1 = r.matcher(amoutTextField.getText());
            if (m.find() && m1.find()) {
                goods = new Goods();
                goods.setIDGoods(Integer.valueOf(IDGoodsTextField.getText()));
                goods.setNameGoods(nameTextField.getText());
                goods.setAmount(Integer.valueOf(amoutTextField.getText()));
                goods.setPrice(Integer.valueOf(priceTextField.getText()));
                goods.setImportDate(ImportDateTextField.getText());
                goodsService.updateGoods(goods);
                JOptionPane.showMessageDialog(rootPane, "Sửa Thành Công!");
                
                new ManageGoodsFrame(u.getIDUser()).setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Giá hoặc Số lượng không hợp lệ!");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed


