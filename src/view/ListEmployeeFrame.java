    private void EditSalaryButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditSalaryButtonActionPerformed
        // TODO add your handling code here:
        int row = EmployeeTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(ListEmployeeFrame.this, "Chọn một nhân viên để xem thông tin");
        } else {
            
            int IDUser = (Integer) EmployeeTable.getValueAt(row, 0);
            new SalaryEFrame(IDUser, user.getIDUser()).setVisible(true);
            this.dispose();
        }
        
    }//GEN-LAST:event_EditSalaryButtonActionPerformed

